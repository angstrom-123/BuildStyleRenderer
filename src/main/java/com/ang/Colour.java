package com.ang;

public class Colour {
	double r;
	double g;
	double b;

	public Colour() {}

	public Colour(double r, double g, double b){
		this.r = r;
		this.g = g;
		this.b = b;
	}

	public double r() {
		return r;

	}

	public double g() {
		return g;

	}
	
	public double b() {
		return b;

	}
}
